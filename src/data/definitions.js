export const getDefinitionById = (id) => {
  return definitions.find(def => def.id === id)
}

export const getDefinitionByAcronym = (acronym) => {
  return definitions.find(def => def.acronym.toUpperCase() === acronym.toUpperCase())
}

export const searchDefinitions = (searchTerm) => {
  const term = searchTerm.toLowerCase()
  return definitions.filter(def =>
    def.acronym.toLowerCase().includes(term) ||
    def.fullName.toLowerCase().includes(term) ||
    def.description.toLowerCase().includes(term)
  )
}
